/* Declare and initialize variables of the following types:
Double: Store a floating-point number.
Boolean: Store true or false.
Char: Store a single letter.
Print each variable with a message describing its value.
Expected Output Example:
The temperature is 36.5 degrees.
Is it raining? false
The first letter of my name is A.
*/

fun main() {

    var float: Double = 36.5

    var bool: Boolean = false

    var character: Char = 'A'

    println("The temperature is $float degrees.")
    println("Is it raining? $bool")
    println("The first letter of my name is $character")
    
}